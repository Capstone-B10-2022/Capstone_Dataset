# Python 3 program to find
# maximum in arr[] of size n
 
# returns maximum
# in arr[] of size n
def largest(arr, n):
 
    return max(arr)
 
# driver code
arr = [10, 324, 45, 90, 9808]
n = len(arr)
 
print(largest(arr, n))